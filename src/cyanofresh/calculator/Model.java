package cyanofresh.calculator;

class Model {
    int xDenominator;
    int xNumerator;

    int yDenominator;
    int yNumerator;

    Model(int xDenominator, int xNumerator, int yDenominator, int yNumerator) {
        this.xDenominator = xDenominator;
        this.xNumerator = xNumerator;

        this.yDenominator = yDenominator;
        this.yNumerator = yNumerator;
    }
}
